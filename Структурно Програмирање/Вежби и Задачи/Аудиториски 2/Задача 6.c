//Да се напише програма за пресметување и печатење на плоштината и
//периметарот на круг. Радиусот на кругот се чита од стандарден влез (тастатура)
//како децимален број.

//L  = 2 * PI * r; периматарот
//P =  PI * r*r; плоштина
#include <stdio.h>
#include <math.h> //за sqrt коренување
#define PI  3.14
int main() {
    float L,
          r,
          P;
    scanf("%f", &r);

    L = 2 * r * PI;
    P = r * r * PI;

    printf("L: %.2f \n", L);
    printf("P: %.2f", P);

    return 0;
}


