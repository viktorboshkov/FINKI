//Да се напише програма која од СВ ќе прочита два цели броеви и ќе ја испечати
//на компјутерскиот екран нивната сума, разлика, производ и остатокот при
////делењето.

#include <stdio.h>

int main() {
   int x,
       y;

    scanf("%d %d", &x, &y);

    printf("%d + %d = %d \n", x, y, x + y);
    printf("%d - %d = %d \n", x, y, x - y);
    printf("%d * %d = %d \n", x, y, x * y);
    printf("%d %% %d = %d \n", x, y, x % y);

    return 0;
}


